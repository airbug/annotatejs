//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../Class');
var Parser = require('./Parser');
var ParserPassAnnotations = require('./ParserPassAnnotations');
var ParserPassBlocks = require('./ParserPassBlocks');
var Script = require('./source/Script');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var JavascriptParser = Class.extend(Parser, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(sourceFile) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        this.sourceDocument = new Script(sourceFile);

        // NOTE BRN: We add all of the parser passes here. The order that they are added is the order that they will be
        // performed.

        this.addParserPass(new ParserPassBlocks());
        this.addParserPass(new ParserPassAnnotations());
    }


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------


    //-------------------------------------------------------------------------------
    // Public Class Methods
    //-------------------------------------------------------------------------------


    //-------------------------------------------------------------------------------
    // Private Class Methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = JavascriptParser;
