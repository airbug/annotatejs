//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('./Class');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var EventListener = Class.declare({

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(listenerFunction, listenerObject) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        this.listenerFunction = listenerFunction;

        this.listenerObject = listenerObject;
    },


    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------

    equals: function(value) {
        if (Class.doesExtend(value, EventListener)) {
            return (value.listenerFunction === this.listenerFunction && value.listenerObject === this.listenerObject);
        }
        return false;
    },


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

    hearEvent: function(event) {
        this.listenerFunction.call(this.listenerObject, event);
    }
});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = EventListener;
