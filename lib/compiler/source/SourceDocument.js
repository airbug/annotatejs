//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../../Class');
var Document = require('../../Document');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var SourceDocument = Class.extend(Document, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(sourceFile) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {SourceFile}
         */
        this.sourceFile = sourceFile;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {SourceFile}
     */
    getSourceFile: function() {
        return this.sourceFile;
    },

    /**
     * @return {string}
     */
    getHomePath: function() {
        return this.sourceFile.getHomePath();
    }

    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------

    //-------------------------------------------------------------------------------
    // Class methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = SourceDocument;
