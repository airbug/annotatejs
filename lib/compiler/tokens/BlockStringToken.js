//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../../Class');
var Token = require('./Token');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var BlockStringToken = Class.extend(Token, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(characterListView, stringType) {

        this._super('block_string', characterListView);


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {string}
         */
        this.stringType = stringType;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------


    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------

    toString: function() {
        var output = "[BlockStringToken] {\n";
        output += "\ttype: " + this.type + "\n";
        output += "}\n";
        return output;
    }

    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Static Variables
//-------------------------------------------------------------------------------

BlockStringToken.StringType = {
    DOUBLE_QUOTE: 'double_quote',
    SINGLE_QUOTE: 'single_quote'
};

//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = BlockStringToken;
