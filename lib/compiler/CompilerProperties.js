//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../Class');
var Map = require('../Map');
var Obj = require('../Obj');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var CompilerProperties = Class.extend(Obj, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function() {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {Map<string, *>}
         */
        this.propertiesNameToPropertyValueMap = new Map();
    },


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

    /**
     * @param {string} name
     * @return {*}
     */
    getProperty: function(name) {
        return this.propertiesNameToPropertyValueMap.get(name);
    },

    /**
     * @param {string} name
     * @param {*} value
     */
    setProperty: function(name, value) {
        this.propertiesNameToPropertyValueMap.put(name, value);
    }
});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = CompilerProperties;
