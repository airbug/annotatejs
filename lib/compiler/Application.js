//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../Class');
var List = require('../List');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var Application = Class.declare({

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function() {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        this.outputDirectory = null;

        this.scriptList = new List();
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    getOutputDirectory: function() {
        return this.outputDirectory;
    },

    setOutputDirectory: function(outputDirectory) {
        this.outputDirectory = outputDirectory;
    },


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

    addScript: function(script) {
        if (!this.scriptList.contains(script)) {
            this.scriptList.add(script);
        }

        //TODO BRN: Send a message about a script being added to the application.
    }
});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = Application;
