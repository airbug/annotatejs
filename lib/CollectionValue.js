//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('./Class');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var CollectionValue = Class.declare({

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(value) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        this.count = 1;

        this.value = value;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @speed O(1)
     */
    decrementCount: function() {
        this.count--;
    },

    /**
     * @return {number}
     */
    getCount: function() {
        return this.count;
    },

    /**
     * @speed O(1)
     */
    incrementCount: function() {
        this.count++;
    },

    /**
     * @return {*}
     */
    getValue: function() {
        return this.value;
    },


    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------

    /**
     * @param {*} value
     * @return {boolean}
     */
    equals: function(value) {
        if (Class.doesExtend(value, CollectionValue)) {
            if (value.getValue() === this.getValue() && value.getCount() === this.getCount()) {
                return true;
            }
        }
        return false;
    }
});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = CollectionValue;
