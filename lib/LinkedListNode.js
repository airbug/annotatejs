//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('./Class');
var Obj = require('./Obj');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var LinkedListNode = Class.extend(Obj, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(value) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------
        
        /**
         * @private
         * @type {Node}
         */
        this.nextNode = null;

        /**
         * @private
         * @type {Node}
         */
        this.previousNode = null;

        /**
         * @private
         * @type {*}
         */
        this.value = value;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {*}
     */
    getValue: function() {
        return this.value;
    },
    
    getNextNode: function() {
        return this.nextNode;
    },

    /**
     * @return {boolean}
     */
    hasNextNode: function() {
        return this.nextNode !== null;
    },

    getPreviousNode: function() {
        return this.previousNode;
    },

    /**
     * @return {boolean}
     */
    hasPreviousNode: function() {
        return this.previousNode !== null;
    },


    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------

    toString: function() {
        var output = "[LinkedListNode] {\n";
        output += "\value:" + this.value.toString();
        output += "}\n";
        return output;
    }

    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Static Variables
//-------------------------------------------------------------------------------


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = LinkedListNode;
