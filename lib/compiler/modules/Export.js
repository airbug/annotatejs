//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../../Class');
var Obj = require('../../Obj');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var Export = Class.extend(Obj, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(exportName, exportAnnotationNode, script) {

        this._super();


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {AnnotationNode}
         */
        this.exportAnnotationNode = exportAnnotationNode;

        /**
         * @private
         * @type {string}
         */
        this.exportName = exportName;

        /**
         * @private
         * @type {Script}
         */
        this.script = script;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {AnnotationNode}
     */
    getExportAnnotationNode: function() {
        return this.exportAnnotationNode;
    },

    /**
     * @return {string}
     */
    getExportName: function() {
        return this.exportName;
    },

    /**
     * @return {Script}
     */
    getScript: function() {
        return this.script;
    }


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = Export;
