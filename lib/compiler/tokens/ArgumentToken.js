//-------------------------------------------------------------------------------
// Requires
//-------------------------------------------------------------------------------

var Class = require('../../Class');
var TokenContainer = require('./TokenContainer');
var TokenTypes = require('./TokenTypes');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var ArgumentToken = Class.extend(TokenContainer, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function(characterListView, tokenList) {

        this._super(TokenTypes.Other.ARGUMENT, characterListView, tokenList);


        //-------------------------------------------------------------------------------
        // Declare Variables
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {ExpressionToken}
         */
        this.expressionToken = tokenList.getAt(0);
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {ExpressionToken}
     */
    getExpressionToken: function() {
        return this.expressionToken;
    }


    //-------------------------------------------------------------------------------
    // Object Implementation
    //-------------------------------------------------------------------------------


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

});


//-------------------------------------------------------------------------------
// Static Variables
//-------------------------------------------------------------------------------


//-------------------------------------------------------------------------------
// Module Export
//-------------------------------------------------------------------------------

module.exports = ArgumentToken;
